# 1. 基础字符串输出（默认换行）
print("hello world")

# 2. 单个数值输出（默认换行）
print(1)

# 3. 多个数值输出（默认空格分隔、换行结尾）
print(1, 2, 3)

# 4. 自定义分隔符 sep（替换默认空格）
print(1, 2, 3, sep="q")

# 5. 自定义结束符 end（实现不换行，连续输出）
print(1, 2, 3, end=", ")
print(1, 2, 3)

# 6. Python 运算优先级：先乘除 * /，后加减 + -
print(2+2*3/4)

